#!/usr/bin/env node
// Auto-generates docs/token-reference.md from token JSON files.
// Run: npm run generate-docs

import { readFileSync, writeFileSync, mkdirSync, readdirSync } from 'node:fs';
import { resolve, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));

function readJson(rel) {
  return JSON.parse(readFileSync(resolve(__dirname, rel), 'utf8'));
}

const core     = readJson('../tokens/core.json');
const semantic = readJson('../tokens/semantic.json');

const brandsDir = resolve(__dirname, '../tokens/brands');
const brandFiles = readdirSync(brandsDir).filter(f => f.endsWith('.json'));
const brands = brandFiles.map(f => ({
  name: basename(f, '.json').replace('brand-', ''),
  data: readJson(`../tokens/brands/${f}`),
}));

// Flatten a nested token object to [{path, value, type, desc}]
function flatten(obj, prefix = '') {
  const out = [];
  for (const [k, v] of Object.entries(obj)) {
    if (k.startsWith('$')) continue;
    const path = prefix ? `${prefix}.${k}` : k;
    if (v !== null && typeof v === 'object' && '$value' in v) {
      out.push({ path, value: v.$value, type: v.$type ?? '—', desc: v.$description ?? '' });
    } else if (typeof v === 'object' && v !== null) {
      out.push(...flatten(v, path));
    }
  }
  return out;
}

function dig(obj, path) {
  return path.split('.').reduce((o, k) => o?.[k], obj);
}

function brandVal(path, brandData) {
  return dig(brandData, path)?.$value ?? null;
}

function esc(s) {
  return String(s).replace(/\|/g, '\\|');
}

// Core section: one table per top-level group
function coreSection(groupKey) {
  const tokens = flatten(core[groupKey] ?? {}, groupKey);
  if (!tokens.length) return '';
  const rows = tokens.map(t =>
    `| \`${esc(t.path)}\` | \`${esc(t.value)}\` | \`${esc(t.type)}\` |`
  ).join('\n');
  return `### \`${groupKey}\`\n\n| Token | Value | Type |\n|-------|-------|------|\n${rows}\n\n`;
}

// Semantic section: table with one column per brand
function semanticSection(groupKey) {
  const tokens = flatten(semantic[groupKey] ?? {}, groupKey);
  if (!tokens.length) return '';
  const brandHeaders = brands.map(b => `${b.name} mode`).join(' | ');
  const rows = tokens.map(t => {
    const brandCols = brands.map(b => `\`${esc(brandVal(t.path, b.data) ?? t.value)}\``).join(' | ');
    return `| \`${esc(t.path)}\` | ${brandCols} |`;
  }).join('\n');
  const dividers = brands.map(() => '---------').join(' | ');
  return `### \`${groupKey}\`\n\n| Token | ${brandHeaders} |\n|-------|${dividers}|\n${rows}\n\n`;
}

const CORE_GROUPS   = ['color', 'radius', 'space', 'font-size', 'font-weight', 'border-width', 'duration', 'easing', 'ring'];
const SEM_GROUPS    = ['action', 'input', 'card', 'badge', 'surface', 'status'];

const coreCount = CORE_GROUPS.reduce((n, g) => n + flatten(core[g] ?? {}).length, 0);
const semCount  = SEM_GROUPS.reduce((n, g) => n + flatten(semantic[g] ?? {}).length, 0);
const brandList = brands.map(b => b.name).join(', ');

const doc = `# Token Reference

> Auto-generated from \`tokens/core.json\` and \`tokens/semantic.json\`.
> **Do not edit this file manually.** Run \`npm run generate-docs\` to refresh.

**${coreCount} core tokens · ${semCount} semantic tokens · brands: ${brandList}**

---

## Core tokens

Raw primitive values. No semantic meaning. Referenced by semantic tokens via \`{alias.path}\` syntax.

${CORE_GROUPS.map(coreSection).join('')}---

## Semantic tokens

Intent-based aliases. Brand columns show which core value is active per brand mode.
Designers switch modes in Figma; developers set \`data-brand="<name>"\` in HTML.

${SEM_GROUPS.map(semanticSection).join('')}`.trimEnd() + '\n';

mkdirSync(resolve(__dirname, '../docs'), { recursive: true });
const outPath = resolve(__dirname, '../docs/token-reference.md');
writeFileSync(outPath, doc);
console.log(`✓ Token reference written → docs/token-reference.md  (${coreCount} core + ${semCount} semantic)`);
